     private void parpadea(){
        counter=1;
         
        cambiaNegro();
        ActionListener listener = new ActionListener(){
            public void actionPerformed(ActionEvent event){
                int n;
                
                n=counter%2;
                switch(n){
                    case 1:cambiaVerde();
                    break;
                    default:
                    cambiaNegro();      
                }
                counter++;
                if (counter >= 6) {
                    timer.stop();
                    cambiaAzul();
                }
                  
                }    
             };   
         if (timer != null && timer.isRunning()) {
            return;
         }
         timer = new Timer(500, listener);
         timer.start();    
     }
     private void llenaMatriz(){
        ii=0;
        jj=0;
        
        cambiaRojo();
        ActionListener listener = new ActionListener(){
            public void actionPerformed(ActionEvent event){
                matrizT[ii][jj].setText(matriz[ii][jj]+"");
                jj++;
                if(jj>8){
                    jj=0;
                    ii++;
                }
                if (ii >8){
                   timer.stop();
                   parpadea();
                } 
                }    
             };   
         if (timer != null && timer.isRunning()) {
            return;
         }
         timer = new Timer(50, listener);
         timer.start(); 
 
     }
